            "okio" -> "okio-lib"
            else -> null
        }

        "javax.inject" -> when (name) {
            "javax.inject" -> "jsr330"
            else -> null
        }

        "org.bouncycastle" -> when (name) {
            "bcpkix-jdk15on" -> "bouncycastle-bcpkix-unbundled"
            "bcpkix-jdk18on" -> "bouncycastle-bcpkix-unbundled"
