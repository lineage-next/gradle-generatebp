            "kotlinx-coroutines-android" -> "kotlinx-coroutines-android"
            "kotlinx-coroutines-core" -> "kotlinx-coroutines-core"
            "kotlinx-coroutines-core-jvm" -> "kotlinx-coroutines-core-jvm"
